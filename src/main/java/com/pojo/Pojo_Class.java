package com.pojo;

public class Pojo_Class {

	// Encapsulation--> Binding of code and data together
	// as a single entity

	// 1. Declare the elements as private
	// 2. Generate the getters and setters
	// 3. Initiialize the elements

	private String name;

	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
